#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		s_len;
	char	*str;
	int		i;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (s_len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
